package Beans;

public class BeanSuperviviente {
    private int idInt;
    private String idSuperviviente;
    private String nombre;
    private String apellido;
    private String sexo;
    private double peso;
    private double fuerza;
    private String nombrePareja;
    private String apellidoPareja;
    private double pesoCargado;


    public String getIdSuperviviente() {
        return idSuperviviente;
    }

    public void setIdSuperviviente(String idSuperviviente) {
        this.idSuperviviente = idSuperviviente;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getFuerza() {
        return fuerza;
    }

    public void setFuerza(double fuerza) {
        this.fuerza = fuerza;
    }

    public String getNombrePareja() {
        return nombrePareja;
    }

    public void setNombrePareja(String nombrePareja) {
        this.nombrePareja = nombrePareja;
    }

    public String getApellidoPareja() {
        return apellidoPareja;
    }

    public void setApellidoPareja(String apellidoPareja) {
        this.apellidoPareja = apellidoPareja;
    }

    public double getPesoCargado() {
        return pesoCargado;
    }

    public void setPesoCargado(double pesoCargado) {
        this.pesoCargado = pesoCargado;
    }

    public int getIdInt() {
        return idInt;
    }

    public void setIdInt(int idInt) {
        this.idInt = idInt;
    }
}