package Beans;

public class BeanHumano {
    private String idHumano;
    private String nombreHumano;
    private String apellidoHumano;
    private String sexoHumano;
    private String estadoHumano;


    public String getNombreHumano() {
        return nombreHumano;
    }

    public void setNombreHumano(String nombreHumano) {
        this.nombreHumano = nombreHumano;
    }

    public String getApellidoHumano() {
        return apellidoHumano;
    }

    public void setApellidoHumano(String apellidoHumano) {
        this.apellidoHumano = apellidoHumano;
    }

    public String getSexoHumano() {
        return sexoHumano;
    }

    public void setSexoHumano(String sexoHumano) {
        this.sexoHumano = sexoHumano;
    }

    public String getEstadoHumano() {
        return estadoHumano;
    }

    public void setEstadoHumano(String estadoHumano) {
        this.estadoHumano = estadoHumano;
    }

    public String getIdHumano() {
        return idHumano;
    }

    public void setIdHumano(String idHumano) {
        this.idHumano = idHumano;
    }
}
